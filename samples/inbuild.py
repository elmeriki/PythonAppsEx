import turtle 

def animation(speed):


    rotate = turtle.Turtle()
    rotate.forward(speed)
    rotate.left(90)
    rotate.forward(speed)
    rotate.left(90)
    rotate.forward(speed)
    rotate.left(90)
    rotate.forward(speed)
   
   
for number in range(2,9):
    animation(300)
turtle.done()
    











